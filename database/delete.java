    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        String query = "delete from contacts where ";
        if(jTextField1.getText().isEmpty())
            jLabel3.setText("Field is Empty !");
        else{
        String field = "'"+jTextField1.getText()+"'";
        if(jComboBox1.getSelectedItem().toString().equals("Roll No."))
        {
            query += "rno="+field;
        }
        if(jComboBox1.getSelectedItem().toString().equals("Name"))
        {
            query += "name="+ field;
        }
        if(jComboBox1.getSelectedItem().toString().equals("Mob. No."))
        {
            query += "mob=" +field;
        }
        try{
            Connection con = DataBase.getConnection();
            Statement stmt = con.createStatement();
            stmt.execute(query);
            JOptionPane.showMessageDialog(this, "Deleted Successfully !!", "Status", JOptionPane.INFORMATION_MESSAGE);
        }
        catch(Exception e){ JOptionPane.showMessageDialog(this, e, "Status", JOptionPane.ERROR_MESSAGE); }
        
        }
    }                                        

